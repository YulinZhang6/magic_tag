// 绘图时备份
unsigned char paintBuffer[11*110] = {0};

static soft_timer_t  *restore_timer;

static void restore_timer_cb(void *args);

void image_buffer_init(void){
    soft_timer_register(&restore_timer, OS_ONE_SHOT_MODE, restore_timer_cb, NULL);
}
// 获取当前屏幕的buffer
unsigned char* getCurInkBuffer(){
    if(cur_ink_idx==0)return inkBuffer;
    // else if(cur_ink_idx==1)return inkBuffer2;
    else return inkBuffer1;
}

// 切换屏幕
void switchInkBuffer(){
    if(isDialogShow)restoreImg();
    cur_ink_idx++;
    cur_ink_idx = cur_ink_idx % 2;
    EpdDisFull(getCurInkBuffer(), 1);
}

// 显示弹窗
void paintDialog()
{
	int16_t width = 88;
	int16_t height = 110;
	int16_t startX = 64;
	int16_t startY = 30;
	int16_t i=0;
	unsigned char* src = getCurInkBuffer();
    if(isDialogShow)return;
	// EPD_DisplayPartBaseImage(src);
	// 备份图像
	for(;i<height;i++){
		memcpy(paintBuffer+i * width/8, src + (200*(startY+i)+200-width-startX)/8, width/8);
	}
    isDialogShow = 1;
	Paint_NewImage(src, 200, 200, ROTATE_0, WHITE);
	Paint_SetMirroring(MIRROR_HORIZONTAL);
	// 填充背景
	Paint_ClearWindows(startX, startY,startX+width,startY+height,WHITE);
	Paint_DrawRectangle(startX+2, startY+2,startX+width-2,startY+height-2,BLACK, DOT_PIXEL_2X2, DRAW_FILL_EMPTY);
	

	// 显示时间
    parseUTCtime(timestr);
    Paint_DrawString_EN(startX+10, startY+3, timestr, &Font20, WHITE, BLACK);

	// 显示温湿度
    getStatus(HUM_TEMP,&BSP_status);
    sprintf(tempstr, "T%.1fC\0",BSP_status.temp);
    sprintf(humstr, "H%d%%\0",BSP_status.hump);
	Paint_DrawString_EN(startX+2, startY+30, tempstr, &Font20, WHITE, BLACK);
	Paint_DrawString_EN(startX+2, startY+60, humstr, &Font20, WHITE, BLACK);

	// 显示天气
	if(weatherstr[0] == 'N' && weatherstr[2] == 'N'){
		report_gps(3);
	}
	Paint_DrawString_EN(startX+4, startY+90, weatherstr, &Font16, WHITE, BLACK);

	EpdDisFull(src, 1);
    // 5s后关闭弹窗
    soft_timer_start(restore_timer, 5000);
}

// 恢复图片buffer
void restoreImg(){
	int16_t width = 88;
	int16_t height = 110;
	int16_t startX = 64;
	int16_t startY = 30;
	int16_t i=0;
	unsigned char* src = getCurInkBuffer();

    // 先停止计时器
    soft_timer_stop(restore_timer);
    isDialogShow = 0;
	// 恢复图像
	for(;i<height;i++){
		memcpy(src + (200*(startY+i)+200-width-startX)/8,paintBuffer+i * width/8, width/8);
	}
}

static void restore_timer_cb(void *args){
    if(isDialogShow){
        restoreImg();
        EpdDisFull(getCurInkBuffer(), 1);
    }
}